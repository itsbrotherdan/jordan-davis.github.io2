function countChars(word, char) {
  var count = 0;
  for (let i = 0; i < word.length; i ++){
    if (word[i] === char){
      count ++
    }
  }
  return count;
  }

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(word) {
  var count = 0;
  for (let i = 0; i < word.length; i ++){
    if (word[i] === 'B'){
      count ++
    }
  }
  return count;
  }
  

